    public void filtrarDatos(String valor)
    {
        String[] titulos = {"idDeduccion", "nombre", "descripcion", "porcentaje", "estatus"}; 
        String[] registros = new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        
        
        String SQL = "select * from Deducciones where nombre like '%" + valor + "%'";
        
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while(rs.next()){
                registros[0]= rs.getString("idDeduccion");
                registros[1]= rs.getString("nombre");
                registros[2]= rs.getString("descripcion");
                registros[3]= rs.getString("porcentaje");
                registros[4]= rs.getString("estatus");
                modelo.addRow(registros);
            }
            tablaDeducciones.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Mostrar Datos " + e.getMessage());
        }
    }
    
    public void mostrarDatos(int inicio, int fin){
        String[] titulos = {"idDeduccion", "nombre", "descripcion", "porcentaje", "estatus"};
        String[] registros = new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        
        String SQL = "select * from Deducciones where estatus = 'A' limit " + inicio + ", " + fin;
        
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while(rs.next()){
                registros[0]= rs.getString("idDeduccion");
                registros[1]= rs.getString("nombre");
                registros[2]= rs.getString("descripcion");
                registros[3]= rs.getString("porcentaje");
                registros[4]= rs.getString("estatus");
                modelo.addRow(registros);
            }
            tablaDeducciones.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Mostrar Datos " + e.getMessage());
        }
    }
    
    public void eliminarRegistros(){
        int filaSeleccionada = tablaDeducciones.getSelectedRow();
        try {
            String SQL = "update Deducciones set estatus= 'I' where idDeduccion = " + tablaDeducciones.getValueAt(filaSeleccionada, 0);
            Statement st = con.createStatement();
            st.executeUpdate(SQL);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar el registro"+ e.getMessage());
        }
    }
    
