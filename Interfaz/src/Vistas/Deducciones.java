            PreparedStatement pst = con.prepareStatement(SQL);
            
            pst.setDouble(1, Integer.parseInt(txt_IdDecuccion.getText()));
            pst.setString(2, txtNombre.getText());
            pst.setString(3, txtDescripcion.getText());
